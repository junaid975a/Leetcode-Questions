class Solution {
  public:
    int stockBuyAndSell(int n, vector<int> &prices) {
        // code here
        int i=0;
        int j=1;
        while(j<n && prices[i]>prices[j]) i++,j++;
        
        int ans = 0;
        while(j<n) {
            if(prices[j]>prices[j-1]) j++;
            else{
                ans += (prices[j-1] - prices[i]);
                i = j;
                j = j+1;
            }
        }
        if(i<n-1) ans += (prices[j-1] - prices[i]);
        return ans;
    }
};


